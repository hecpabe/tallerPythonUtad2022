

# Operadores numéricos
num1 = 2
num2 = 3

print("SUMA")
num3 = num1 + num2
print(num3)

print("RESTA")
num3 = num2 - num1
print(num3)

print("PRODUCTO")
num3 = num1 * num2
print(num3)

print("DIVISIÓN EXACTA")
num3 = num2 / num1
print(num3)

print("DIVISIÓN ENTERA")
num3 = num2 // num1
print(num3)

print("POTENCIA")
num3 = num2 ** num1
print(num3)

# Operadores lógicos
# Operadores de comparación
print("MENOR <")
print(num1 < 1)
print(num1 < 2)
print(num1 < 3)

print("MAYOR >")
print(num1 > 1)
print(num1 > 2)
print(num1 > 3)

print("MENOR O IGUAL <=")
print(num1 <= 1)
print(num1 <= 2)
print(num1 <= 3)

print("MAYOR O IGUAL >=")
print(num1 >= 1)
print(num1 >= 2)
print(num1 >= 3)

print("IGUAL ==")
print(num1 == 1)
print(num1 == 2)
print(num1 == 3)

print("DISTINTO !=")
print(num1 != 1)
print(num1 != 2)
print(num1 != 3)

# Otros operadores lógicos
print("Y -> AND")
print(num1 == 2 and num2 == 3)
print(num1 == 2 and num2 == 2)
print(num1 == 3 and num2 == 3)
print(num1 == 3 and num2 == 2)

print("O -> OR")
print(num1 == 2 or num2 == 3)
print(num1 == 2 or num2 == 2)
print(num1 == 3 or num2 == 3)
print(num1 == 3 or num2 == 2)

