

# Bucles for
print("PRIMER FOR")
for i in range(5):
    print(i)

print("SEGUNDO FOR")
for i in range(5, 10):
    print(i)

print("TERCER FOR")
for i in range(0, 10, 2):
    print(i)

print("CUARTO FOR")
for i in range(0, -5, -1):
    print(i)

print("QUINTO FOR")
lista = [1, 2, 3, "Pepe", 4, "Hola", True]
for i in lista:
    print(i)
